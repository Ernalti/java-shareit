CREATE TABLE IF NOT EXISTS users (
    id INT GENERATED BY DEFAULT AS IDENTITY NOT NULL PRIMARY KEY,
    name VARCHAR,
    email VARCHAR,
    CONSTRAINT email UNIQUE (email)


);
CREATE TABLE IF NOT EXISTS items (
    id INT GENERATED BY DEFAULT AS IDENTITY NOT NULL PRIMARY KEY,
    name VARCHAR,
    description VARCHAR,
    available BOOLEAN,
    owner INT NOT NULL REFERENCES users (id) ON DELETE CASCADE
);

CREATE TABLE IF NOT EXISTS booking (
    id INT GENERATED BY DEFAULT AS IDENTITY NOT NULL PRIMARY KEY,
    start_booking DATETIME,
    end_booking DATETIME,
    item INT NOT NULL REFERENCES items (id) ON DELETE CASCADE,
    booker INT NOT NULL REFERENCES users (id) ON DELETE CASCADE,
    status VARCHAR NOT NULL
);

CREATE TABLE IF NOT EXISTS comments (
    id INT GENERATED BY DEFAULT AS IDENTITY NOT NULL,
    text VARCHAR NOT NULL,
    item INT NOT NULL,
    author INT NOT NULL,
    created DATETIME NOT NULL,
    CONSTRAINT pk_comments PRIMARY KEY (id),
    CONSTRAINT fk_author_name_cm FOREIGN KEY (author) REFERENCES users (id),
    CONSTRAINT fk_item_id_cm FOREIGN KEY (item) REFERENCES items (id)
);